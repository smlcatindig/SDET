package class5;

public class E5LogicalOperators {
    public static void main(String[] args) {

        /*
        NOT ! it flips the boolean values for example if the value is true it will change it to false
        and vice versa -- only works with boolean data type
         */
        boolean flag=false;
        // ! operator will reverse the condition
        // and it will be printed
        System.out.println(!flag);
        System.out.println(flag);


    }
}
