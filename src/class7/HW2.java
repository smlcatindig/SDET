package class7;

public class HW2 {
    public static void main(String[] args) {
        int number=1;
        while(number<=100){
            System.out.print(number+" ");
            number++;
        }
        System.out.println();
        for(int n=1; n<=100; n++){
            System.out.print(n+" ");
        }
        System.out.println();
        int nu=1;
        do{
            System.out.print(nu+" ");
            nu++;
        }while(nu<=100);
    }
}
