package class7;

public class HW1 {
    public static void main(String[] args) {
        int variable=1;
        while(variable<=100){
            System.out.print(variable+" ");
            variable++;
        }
        System.out.println();
        for(int v=1; v<=100; v++){
            System.out.print(v+" ");
        }
        System.out.println();
        int va=1;
        do{
            System.out.print(va+" ");
            va++;
        }while(va<=100);
    }
}
